/**
 * Fuzzy matches a country name or alias to an ISO code
 */
export function normalizeCountryCode(input) {
  if (!input) return null;
  const s = input.trim().toUpperCase();
  
  // Direct ISO match
  if (COUNTRY_DB[s]) return s;

  const normalizedInput = input.trim().toLowerCase();

  for (const [code, data] of Object.entries(COUNTRY_DB)) {
    // Name match
    if (data.name.toLowerCase() === normalizedInput) return code;
    
    // Alias match
    if (data.aliases?.some(alias => alias.toLowerCase() === normalizedInput)) return code;
    
    // Fuzzy substring match
    if (data.name.toLowerCase().includes(normalizedInput) || normalizedInput.includes(data.name.toLowerCase())) {
        return code;
    }
  }

  return null;
}

export function getCountryInfo(input) {
  const code = normalizeCountryCode(input);
  return code ? { code, ...COUNTRY_DB[code] } : null;
}
